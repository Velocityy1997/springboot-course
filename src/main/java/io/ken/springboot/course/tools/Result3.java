package io.ken.springboot.course.tools;

public class Result3<T> {
	
	private Integer code;
    private String msg;
    private T rows;
    private Long total;
    
    
    public Result3() {
        super();
    }

    
    public Result3(Integer code, String msg, T rows,Long total) {
        this.code = code;
        this.msg = msg;
        this.rows = rows;
        this.total =total;
    }


	public Integer getCode() {
		return code;
	}


	public void setCode(Integer code) {
		this.code = code;
	}


	public String getMsg() {
		return msg;
	}


	public void setMsg(String msg) {
		this.msg = msg;
	}


	public T getRows() {
		return rows;
	}


	public void setRows(T rows) {
		this.rows = rows;
	}


	public Long getTotal() {
		return total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}


	@Override
	public String toString() {
		return "Result3 [code=" + code + ", msg=" + msg + ", rows=" + rows + ", total=" + total + "]";
	}


	

    
    
    
}
