package io.ken.springboot.course.bean.paper;

/**
 * 识图用图的考试题
 * @author GouYudong
 * @create 2020-01-15 10:49
 **/
public class PicturePaper {

    private  Integer number;
    private  String table;
    private  String question_code;
    private  String type;//1代表填空，2代表单选，3代表多选；操作题？？？
    private  String question_name;
    private  String select;//选择题选项，非选择题为空
    private  String result;
    private  byte[] img;

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer
                                  number) {
        this.number = number;
    }

    public String getTable() {
        return table;
    }

    public void setTable(String table) {
        this.table = table;
    }

    public String getQuestion_code() {
        return question_code;
    }

    public void setQuestion_code(String question_code) {
        this.question_code = question_code;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getQuestion_name() {
        return question_name;
    }

    public void setQuestion_name(String question_name) {
        this.question_name = question_name;
    }

    public String getSelect() {
        return select;
    }

    public void setSelect(String select) {
        this.select = select;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public byte[] getImg() {
        return img;
    }

    public void setImg(byte[] img) {
        this.img = img;
    }
}
