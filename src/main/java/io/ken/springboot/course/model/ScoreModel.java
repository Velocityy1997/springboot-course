public class ScoreModel {

	private String examId;//试卷id
	private String name;//考试人名
	private String className;//班级
	private String refrenceState;//考试状态
	private String score;//得分
	private String subject;//查看
	
	
	public String getExamId() {
		return examId;
	}
	public void setExamId(String examId) {
		this.examId = examId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getRefrenceState() {
		return refrenceState;
	}
	public void setRefrenceState(String refrenceState) {
		this.refrenceState = refrenceState;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
			
}
